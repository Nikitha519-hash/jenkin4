public class sum {
    public static void main(String[] args) {
        // Declare and initialize two numbers
        int num1 = 10;
        int num2 = 20;

        // Calculate the sum of the two numbers
        int sum1 = num1 + num2;

        // Output the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is " + sum1);
    }
}

